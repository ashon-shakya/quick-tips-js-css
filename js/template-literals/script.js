const firstName = "Prem";
const lastName = "Acharya";
const address = "Sydney";

// string concatenation using + operator
console.log(
  "Hi! my name is " +
    firstName +
    " " +
    lastName +
    " and I live in " +
    address +
    "."
);

// string concatenation using template literals
console.log(
  `Hi! my name is ${firstName} ${lastName} and I live in ${address}.`
);
